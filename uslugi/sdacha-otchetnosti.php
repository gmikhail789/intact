                    <p>Годовая декларация для ТОО на общеустановленном режиме. Самая объёмная форма отчётности — включает расчёт совокупного годового дохода, вычетов, корректировок, налогооблагаемого дохода и итоговой суммы КПН. Ставка КПН — 20% от налогооблагаемого дохода.</p>

                    <p>Подготовка ФНО 100 — это кульминация всей годовой работы бухгалтера. Нужно правильно определить совокупный годовой доход (включая все виды доходов: от реализации, прочие, доходы от прироста стоимости), применить все законные вычеты (расходы по реализации, общие и административные, вычеты по фиксированным активам), учесть корректировки и перенос убытков прошлых лет. Ошибка в ФНО 100 может привести к переплате КПН на сотни тысяч тенге или, наоборот, к занижению и последующим санкциям.</p>

                    <p><strong>Срок сдачи:</strong> до 31 марта года, следующего за отчётным. Авансовые платежи по КПН — ежемесячно до 25 числа.</p>

