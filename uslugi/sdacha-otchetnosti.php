                    <h2>Какие формы отчётности сдают ИП и ТОО в Казахстане</h2>

                    <p>Набор отчётных форм зависит от формы организации и режима налогообложения. Вот основные:</p>

                    <h3>ФНО 910.00 — упрощённая декларация для ИП и ТОО</h3>

                    <p>Самая распространённая форма для малого бизнеса. Её сдают ИП и ТОО, работающие на упрощённом режиме налогообложения (специальный налоговый режим на основе упрощённой декларации). Форма включает расчёт дохода за полугодие, начисление ИПН (или КПН для ТОО) в размере 3% от дохода, а также социальные платежи за работников и за самого ИП.</p>

                    <p>Несмотря на название «упрощённая», форма 910 требует аккуратного заполнения. Нужно правильно определить облагаемый доход (не все поступления на счёт являются доходом — например, возвраты от контрагентов, переводы между собственными счетами), рассчитать социальные платежи с учётом минимальных и максимальных лимитов, корректно отразить данные по сотрудникам, если они есть.</p>

                    <p><strong>Срок сдачи:</strong> не позднее 15 числа второго месяца, следующего за отчётным полугодием. То есть за первое полугодие — до 15 августа, за второе — до 15 февраля.</p>

                    <p><strong>Срок уплаты налогов:</strong> до 25 числа второго месяца после полугодия (25 августа и 25 февраля).</p>

                    <h3>ФНО 200.00 — по индивидуальному подоходному налогу и социальному налогу</h3>

                    <p>Эту форму сдают все работодатели — ИП и ТОО, у которых есть наёмные сотрудники или которые начисляют доходы физическим лицам (например, по договорам ГПХ). В форме отражаются: ИПН, удержанный у источника выплаты, социальный налог, обязательные пенсионные взносы (ОПВ), социальные отчисления (СО), взносы и отчисления на обязательное медицинское страхование (ОСМС/ВОСМС).</p>

                    <p>Форма 200 — одна из самых «капризных»: любая ошибка в расчёте зарплатных налогов отражается здесь. Неправильно рассчитанный ИПН, неучтённый стандартный вычет, ошибка в ставке социальных отчислений — и при камеральном контроле налоговая обнаружит расхождение. Мы формируем ФНО 200 автоматически из данных <a href="/uslugi/kadrovyj-uchet.php">зарплатного расчёта в 1С</a>, что исключает ручные ошибки.</p>

                    <p><strong>Срок сдачи:</strong> ежеквартально, до 15 числа второго месяца, следующего за отчётным кварталом.</p>

                    <h3>ФНО 300.00 — декларация по НДС</h3>

                    <p>Форма обязательна для плательщиков налога на добавленную стоимость. Встать на учёт по НДС нужно при превышении оборота в 10 000 МРП за календарный год (около 43,25 млн ₸ в 2026 году). Декларация по НДС — одна из самых сложных форм: в ней отражаются все облагаемые обороты, входящий и исходящий НДС, корректировки, обороты по нулевой ставке и необлагаемые обороты.</p>

                    <p>Ошибки в ФНО 300 обходятся дорого. Если вы занизили исходящий НДС — доначисление плюс штраф 50% от суммы занижения. Если неправомерно приняли входящий НДС к зачёту (например, от контрагента, не являющегося плательщиком НДС) — аналогичные последствия. Мы проверяем каждую операцию: сверяем данные с реестром ЭСФ, контролируем корректность зачёта, формируем декларацию из данных 1С с последующей ручной проверкой.</p>

                    <p><strong>Срок сдачи:</strong> ежеквартально, до 15 числа второго месяца после отчётного квартала.</p>

