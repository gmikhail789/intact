                <p class="lead">Решили начать бизнес? Первый шаг — правильная регистрация. От выбора формы (ИП или ТОО), кодов ОКЭД и режима налогообложения зависит, сколько вы будете платить налогов, какую отчётность сдавать и с какими ограничениями столкнётесь. Ошибка на старте может стоить сотни тысяч тенге в год.</p>

                <p>Intact регистрирует ИП и ТОО в Алматы под ключ: от консультации по выбору формы бизнеса до открытия банковского счёта. А когда приходит время закрыть бизнес — проводим грамотную ликвидацию без долгов и штрафов.</p>

                <h2>Регистрация ИП в Казахстане: пошаговый процесс</h2>

                <p>Индивидуальный предприниматель — самая простая форма ведения бизнеса в Казахстане. Подходит для фрилансеров, мастеров, консультантов, мелкой торговли и услуг.</p>

                <h3>Шаг 1: Выбор вида деятельности (ОКЭД)</h3>

                <p>ОКЭД (Общий классификатор видов экономической деятельности) — это коды, которые определяют, чем вы будете заниматься. Важно выбрать правильные коды с самого начала: от ОКЭД зависят доступные режимы налогообложения, необходимость получения лицензий и разрешений. Мы подбираем ОКЭД так, чтобы покрыть все ваши текущие и планируемые виды деятельности.</p>

                <h3>Шаг 2: Выбор режима налогообложения</h3>

                <p>Для ИП доступны несколько режимов: патент (1% от дохода, без сотрудников), упрощённая декларация (3% от дохода, до 30 сотрудников), фиксированный вычет и общеустановленный. Мы рассчитываем налоговую нагрузку по каждому варианту и рекомендуем оптимальный — подробнее на странице <a href="/uslugi/nalogovoe-soprovozhdenie.php">налогового сопровождения</a>.</p>

                <h3>Шаг 3: Регистрация через eGov.kz</h3>

                <p>Регистрация ИП в Казахстане бесплатна и занимает 1 рабочий день. Заявление подаётся через портал eGov.kz с использованием ЭЦП (электронной цифровой подписи). Мы готовим заявление, подаём его и получаем уведомление о начале деятельности.</p>

                <h3>Шаг 4: Получение ЭЦП и регистрация в кабинете налогоплательщика</h3>

                <p>ЭЦП необходима для работы с государственными порталами: eGov, кабинет налогоплательщика, eSalyk, ИС ЭСФ. Мы помогаем получить ЭЦП в Центре обслуживания населения (ЦОН) или через eGov и настроить доступ ко всем необходимым системам.</p>

                <h3>Шаг 5: Открытие банковского счёта</h3>

                <p>Для ведения бизнеса нужен расчётный счёт. Мы подскажем, какой банк выбрать с учётом ваших потребностей (тарифы, интернет-банкинг, интеграция с 1С), и поможем подготовить документы для открытия.</p>

                <h2>Регистрация ТОО в Казахстане</h2>

                <p>Товарищество с ограниченной ответственностью — форма для бизнеса, который планирует расти: привлекать инвесторов, работать с крупными клиентами, нанимать больше сотрудников.</p>

                <p>Процесс регистрации ТОО включает дополнительные шаги по сравнению с ИП:</p>

                <p><strong>Устав.</strong> ТОО обязано иметь устав — учредительный документ, определяющий структуру управления, права участников, порядок принятия решений. Мы готовим устав с учётом ваших пожеланий и требований законодательства.</p>

                <p><strong>Уставный капитал.</strong> Минимальный размер уставного капитала для субъектов малого предпринимательства — 0 (ноль) тенге. Для среднего и крупного бизнеса — 100 МРП. Мы определяем оптимальный размер уставного капитала.</p>

                <p><strong>Юридический адрес.</strong> ТОО обязано иметь юридический адрес. Это может быть арендованное помещение, собственная недвижимость или адрес регистрации учредителя (для малого бизнеса).</p>

                <p><strong>Госпошлина.</strong> Регистрация ТОО через eGov.kz бесплатна (электронная регистрация). При подаче через ЦОН — госпошлина зависит от категории бизнеса.</p>

                <p><strong>Сроки:</strong> электронная регистрация — 1 рабочий день. Полный цикл с уставом, банковским счётом и налоговой постановкой — 3-5 рабочих дней.</p>

                <h2>Что нужно решить до регистрации бизнеса</h2>

                <p>Регистрация — технически несложная процедура. Сложнее то, что предшествует ей: стратегические решения, которые определят вашу налоговую нагрузку на годы вперёд.</p>

                <p><strong>ИП или ТОО?</strong> Этот выбор зависит от масштаба бизнеса, планов по привлечению партнёров или инвесторов, видов деятельности и уровня личной ответственности. ИП отвечает всем своим имуществом — включая личную квартиру и автомобиль. ТОО формально отвечает в пределах уставного капитала, хотя на практике кредиторы могут добиться привлечения учредителя к субсидиарной ответственности.</p>

                <p><strong>Какой режим налогообложения?</strong> От этого зависит ставка налога: патент (1%), упрощённка (3%), общий режим (20% КПН от прибыли). Неправильный выбор на старте может стоить сотни тысяч тенге в год. Подробнее — на странице <a href="/uslugi/nalogovoe-soprovozhdenie.php">налогового сопровождения</a>.</p>

                <p><strong>Какие ОКЭД?</strong> Правильный выбор кодов ОКЭД — не формальность. От них зависит доступность определённых режимов налогообложения и необходимость лицензий. Некоторые виды деятельности несовместимы с упрощённым режимом. Мы подбираем ОКЭД так, чтобы покрыть все ваши текущие и планируемые направления, при этом не создавая лишних ограничений.</p>

                <p><strong>Нужна ли постановка на учёт по НДС?</strong> Обязательна при обороте выше 10 000 МРП в год. Но есть случаи, когда добровольная постановка выгодна и при меньшем обороте — например, если ваши покупатели сами плательщики НДС и просят выставлять ЭСФ с НДС.</p>

                <p>Все эти вопросы мы разбираем на бесплатной консультации до начала регистрации. Вы получаете конкретные рекомендации с расчётами.</p>

                <h2>Что происходит после регистрации</h2>

                <p>Получить свидетельство о регистрации — это только начало. Чтобы бизнес мог полноценно работать, нужно выполнить ещё ряд шагов, о которых многие начинающие предприниматели забывают:</p>

                <p><strong>Получить ЭЦП.</strong> Электронная цифровая подпись необходима для работы с eGov, кабинетом налогоплательщика, eSalyk и ИС ЭСФ. Без ЭЦП вы не сможете сдать отчётность и выставлять электронные счета-фактуры.</p>

                <p><strong>Зарегистрироваться в кабинете налогоплательщика.</strong> Здесь вы будете видеть начисленные налоги, сдавать отчётность, получать уведомления от КГД. Настройка кабинета — обязательный шаг.</p>

                <p><strong>Настроить eSalyk.</strong> Мобильное приложение для сдачи упрощённой декларации ФНО 910. Для ИП на упрощёнке — основной инструмент взаимодействия с налоговой.</p>

                <p><strong>Зарегистрироваться в ИС ЭСФ.</strong> Система электронных счетов-фактур обязательна для всех предпринимателей, осуществляющих реализацию товаров и услуг. Без ЭСФ вы не сможете продавать — контрагенты будут требовать электронные документы.</p>

                <p><strong>Открыть расчётный счёт.</strong> Для ведения безналичных расчётов необходим счёт в банке. Мы подскажем оптимальный банк с учётом ваших потребностей: интернет-банкинг, мобильное приложение, тарифы на обслуживание, интеграция с 1С.</p>

                <p><strong>Встать на учёт в органах статистики.</strong> В зависимости от вида деятельности вам может быть необходимо сдавать статистическую отчётность. Мы проверяем, какие формы обязательны для вашего ОКЭД.</p>

