                <h2>Ликвидация ИП и ТОО в Алматы</h2>

                <p>Бизнес не всегда работает вечно. Если пришло время закрыть ИП или ТОО, важно сделать это правильно. Формальная ликвидация «просто забросить» — опасна: продолжают начисляться социальные платежи, формируется задолженность, копятся штрафы за несданную отчётность.</p>

                <h3>Ликвидация ИП</h3>
                <p>Процесс относительно прост: сдать все формы отчётности за текущий период (включая ФНО 910 за незавершённое полугодие), оплатить все налоги и социальные платежи, получить справку об отсутствии задолженности из кабинета налогоплательщика, подать заявление о прекращении деятельности через eGov или ЦОН. Срок — от 5 рабочих дней при отсутствии задолженности.</p>

                <p>Важный нюанс: даже если вы фактически не вели деятельность, но ИП числится как действующий — за вами числятся обязательства по минимальным социальным платежам. При ликвидации эту задолженность нужно погасить. Мы рассчитаем точную сумму и поможем закрыть все вопросы.</p>

                <h3>Ликвидация ТОО</h3>
                <p>Более сложный процесс, включающий: решение учредителей о ликвидации, назначение ликвидационной комиссии, публикацию объявления в СМИ (обязательно — в газете «Юридическая газета» или на портале elicense.kz), уведомление кредиторов, налоговую проверку (камеральную), подготовку промежуточного и окончательного ликвидационного баланса, исключение из Национального реестра бизнес-идентификационных номеров. Срок — от 2 до 6 месяцев в зависимости от сложности.</p>

                <p>Частая ошибка — пытаться ликвидировать ТОО самостоятельно и пропустить обязательный этап (например, публикацию или уведомление кредиторов). В результате регистрирующий орган отказывает в исключении из реестра, и процедуру приходится начинать заново. Intact проводит ликвидацию по чёткому чек-листу, исключая подобные ошибки.</p>

                <p>Отдельная ситуация — «брошенные» ТОО, которые не ведут деятельность, но формально числятся в реестре. За них продолжают начисляться обязательства по сдаче отчётности, и рано или поздно формируются штрафы. Если у вас есть такое ТОО — не откладывайте ликвидацию, каждый месяц простоя увеличивает задолженность.</p>

