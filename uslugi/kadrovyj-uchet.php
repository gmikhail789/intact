                <h2>Что входит в кадровый учёт</h2>

                <p>Кадровый учёт — это систематическая работа с документами, которые регулируют трудовые отношения между вами и вашими сотрудниками. Трудовой кодекс РК обязывает каждого работодателя вести кадровую документацию. Вот что мы делаем:</p>

                <p><strong>Трудовые договоры.</strong> Составляем и оформляем трудовые договоры, соответствующие Трудовому кодексу РК. Каждый договор содержит все обязательные условия: место работы, трудовая функция, срок, режим работы, оплата труда, условия расторжения. Дополнительные соглашения при изменении условий.</p>

                <p><strong>Кадровые приказы.</strong> Приказы о приёме на работу, переводе, предоставлении отпуска, командировке, увольнении, поощрении и дисциплинарном взыскании. Каждый приказ оформляется в соответствии с унифицированными формами и регистрируется в журнале.</p>

                <p><strong>Табель учёта рабочего времени.</strong> Ежемесячно формируем табель по каждому сотруднику: отработанные дни, больничные, отпуска, прогулы, сверхурочные. Табель — основа для расчёта заработной платы.</p>

                <p><strong>Штатное расписание.</strong> Документ, определяющий структуру компании, наименование должностей, количество штатных единиц и оклады. Обновляется при изменении структуры или условий оплаты.</p>

                <p><strong>Личные дела сотрудников.</strong> Карточки Т-2, копии документов, сведения об образовании, стаже, льготах. Систематизированное хранение и обновление.</p>

                <p><strong>График отпусков.</strong> Обязательный документ, утверждаемый ежегодно. Мы контролируем соблюдение графика и напоминаем о необходимости предоставить отпуск (по ТК РК работодатель обязан предоставлять отпуск минимум 24 календарных дня в год).</p>

                <h2>Расчёт заработной платы в Казахстане: что нужно знать</h2>

                <p>Расчёт зарплаты в Казахстане — это не просто «оклад минус налоги». Это система из нескольких обязательных платежей, часть из которых удерживается из зарплаты сотрудника, а часть — платится работодателем за свой счёт.</p>

                <h3>Удержания из заработной платы сотрудника</h3>

                <p><strong>Обязательные пенсионные взносы (ОПВ)</strong> — 10% от начисленной зарплаты. Перечисляются в ЕНПФ (Единый накопительный пенсионный фонд). Максимальный предел — 50 МЗП (минимальных заработных плат).</p>

                <p><strong>Взносы на обязательное социальное медицинское страхование (ВОСМС)</strong> — 2% от начисленной зарплаты. Перечисляются в Фонд ОСМС.</p>

                <p><strong>Индивидуальный подоходный налог (ИПН)</strong> — 10% от суммы после вычета ОПВ, ВОСМС и стандартного налогового вычета (14 МРП в месяц, если доход не превышает 25 МРП). Фактическая ставка ИПН на руки — обычно 7-9% от оклада.</p>

                <h3>Платежи за счёт работодателя</h3>

                <p><strong>Социальные отчисления (СО)</strong> — 3.5% от начисленной зарплаты (минус ОПВ). Перечисляются в Государственный фонд социального страхования.</p>

                <p><strong>Отчисления на ОСМС</strong> — 3% от начисленной зарплаты. Платит работодатель дополнительно к ВОСМС сотрудника.</p>

                <p><strong>Социальный налог</strong> — 9.5% от начисленной зарплаты, минус сумма СО. Уплачивается в бюджет.</p>

                <p><strong>Обязательные профессиональные пенсионные взносы (ОППВ)</strong> — 5% для сотрудников, занятых на тяжёлых и вредных работах.</p>

