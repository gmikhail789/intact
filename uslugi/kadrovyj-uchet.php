                <h2>Типичные ошибки в кадровом учёте и их последствия</h2>

                <p>Кадровые нарушения — одна из самых частых причин штрафов при проверке трудовой инспекцией. Вот что мы видим у новых клиентов:</p>

                <p><strong>Отсутствие трудовых договоров.</strong> Сотрудник работает «на словах», без оформления. Штраф — от 20 до 50 МРП (73 840 — 216 250 ₸) за каждого неоформленного работника. Кроме того, сотрудник может подать жалобу в трудовую инспекцию и потребовать компенсацию.</p>

                <p><strong>Неправильный расчёт отпускных.</strong> Частая ошибка — расчёт среднего заработка только по окладу, без учёта премий и надбавок. Это занижает сумму отпускных и является нарушением ТК РК. Сотрудник может потребовать пересчёт и компенсацию.</p>

                <p><strong>Отсутствие табеля учёта рабочего времени.</strong> Без табеля невозможно подтвердить правомерность начисленной зарплаты. При проверке инспектор затребует табели за последние 3 года.</p>

                <p><strong>Ошибки в расчёте зарплатных налогов.</strong> Неправильно рассчитанные ОПВ, ВОСМС или ИПН приводят к расхождениям в <a href="/uslugi/sdacha-otchetnosti.php">форме ФНО 200</a> и камеральным уведомлениям от КГД. Исправление ошибок задним числом — долгий и неприятный процесс.</p>

                <p><strong>Нарушение порядка увольнения.</strong> Увольнение без соблюдения процедуры (предупреждение, основания, оформление приказа) — основание для восстановления сотрудника на работе через суд с выплатой средней зарплаты за всё время вынужденного прогула.</p>

