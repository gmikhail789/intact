                    <h2>Легальная оптимизация налогов для ИП и ТОО</h2>

                    <p>Оптимизация налогов — это не уклонение. Это использование всех предусмотренных Налоговым кодексом РК возможностей для снижения налоговой нагрузки. Вот что мы делаем в рамках налогового сопровождения:</p>

                    <p><strong>Выбор оптимального режима.</strong> Как показано выше, разница между режимами может составлять сотни тысяч тенге в год. Пример: ИП-консультант с доходом 12 млн ₸/год на патенте заплатит 120 000 ₸ налога. На упрощёнке — 360 000 ₸. Разница — 240 000 ₸/год.</p>

                    <p><strong>Максимизация вычетов.</strong> На общеустановленном режиме мы анализируем все расходы компании и обеспечиваем корректное документальное подтверждение каждого вычета. Нередко компании теряют право на вычеты из-за неправильного оформления документов — мы это исключаем. Типичные упущения: представительские расходы без подтверждающих документов (протоколы, списки участников), командировочные без приказов и посадочных талонов, расходы на ГСМ без путевых листов. Каждый из этих расходов мог бы уменьшить налогооблагаемый доход — но без правильного оформления он становится невычитаемым.</p>

                    <p><strong>Оптимизация НДС.</strong> Если вы стали плательщиком НДС, важно правильно учитывать входящий НДС и своевременно получать зачёт. Мы контролируем, чтобы каждый входящий ЭСФ от контрагента-плательщика НДС был корректно отражён в учёте и принят к зачёту. Также следим за тем, чтобы ваши поставщики выставляли ЭСФ вовремя — пропущенный электронный счёт-фактура означает потерю права на зачёт входящего НДС.</p>

                    <p><strong>Мониторинг льгот и послаблений.</strong> Налоговый кодекс РК предусматривает ряд льгот для отдельных видов деятельности и категорий налогоплательщиков. Например, пониженные ставки социального налога для определённых категорий, льготы по НДС для экспортёров, специальные режимы для IT-компаний и приоритетных отраслей. Мы отслеживаем, какие льготы применимы к вашему бизнесу, и помогаем ими воспользоваться.</p>

