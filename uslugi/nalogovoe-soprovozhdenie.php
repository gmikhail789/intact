                    <h2>Режимы налогообложения в Казахстане: как выбрать оптимальный</h2>

                    <p>Выбор режима налогообложения — одно из важнейших решений для бизнеса. Неправильный выбор означает переплату налогов или, наоборот, нарушение закона. В Казахстане действуют следующие режимы:</p>

                    <h3>Общеустановленный режим</h3>

                    <p>Подходит для ТОО и крупных ИП с высокими расходами. Ставка КПН — 20% от разницы между доходами и вычетами. Главное преимущество — возможность учитывать расходы и уменьшать налогооблагаемую базу. Если расходы составляют значительную часть доходов (производство, торговля с высокой себестоимостью), этот режим может быть выгоднее упрощённого.</p>

                    <p><strong>Для кого:</strong> ТОО с оборотом от 200 млн ₸/год, компании с существенными документально подтверждёнными расходами, плательщики НДС.</p>

                    <h3>Упрощённый режим (упрощённая декларация)</h3>

                    <p>Самый популярный режим для малого бизнеса в Казахстане. Ставка — 3% от дохода (для ИП — 1.5% ИПН + 1.5% социальный налог). Ограничения: оборот до 10 000 МРП за полугодие (около 43,25 млн ₸ в 2026), численность сотрудников — до 30 человек.</p>

                    <p><strong>Для кого:</strong> ИП и ТОО со стабильным доходом, невысокой долей расходов и небольшим штатом.</p>

                    <h3>Режим на основе патента</h3>

                    <p>Максимально простой режим для ИП без наёмных сотрудников. Ставка — всего 1% от дохода. Ограничения: оборот до 3 528 МРП за год (около 13 млн ₸), только определённые виды деятельности, отсутствие наёмных работников.</p>

                    <p><strong>Для кого:</strong> самозанятые, фрилансеры, индивидуальные консультанты, мастера.</p>

                    <h3>Фиксированный вычет</h3>

                    <p>Для ИП, которые хотят учитывать расходы, но не хотят вести полноценный учёт. Позволяет вычесть фиксированные 30% от дохода без подтверждающих документов плюс документально подтверждённые расходы на фонд оплаты труда.</p>

