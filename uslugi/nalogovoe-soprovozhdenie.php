                    <h2>Как проходит камеральный контроль и чем мы помогаем</h2>

                    <p>Камеральный контроль — это автоматизированная проверка данных из вашей отчётности, банковских выписок, ЭСФ и информации от контрагентов. Система КГД сопоставляет цифры и выявляет расхождения. Если они обнаружены — вы получаете уведомление с требованием предоставить пояснения или внести корректировки.</p>

                    <p>Типичные причины уведомлений: расхождение данных ЭСФ и декларации по НДС (ФНО 300), несоответствие доходов в ФНО 910 и поступлений на расчётный счёт, разница между данными по зарплате в ФНО 200 и информацией о перечисленных ОПВ в ЕНПФ.</p>

                    <p>Intact анализирует каждое уведомление, определяет причину расхождения и готовит ответ. Если расхождение вызвано ошибкой — мы подаём корректирующую форму. Если расхождение — результат некорректного алгоритма камерального контроля (что тоже бывает) — готовим мотивированное пояснение. Вы получаете готовый результат, а не стресс.</p>

